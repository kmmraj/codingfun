package com.test.math;

public class MathScratchPad {
    public static void main(String[] args) {
        System.out.println("ceil of 3.2 is " + Math.ceil(3.2));
        System.out.println("ceil of 3.8 is " + Math.ceil(3.8));
        System.out.println("floor of 3.2 is " + Math.floor(3.2));
        System.out.println("floor of 3.8 is " + Math.floor(3.8));
        System.out.println("round of 3.2 is " + Math.round(3.2));
        System.out.println("round of 3.8 is " + Math.round(3.8));
        System.out.println("round of 3.5 is " + Math.round(3.5));
        System.out.println("round of 3.6 is " + Math.round(3.6));
        System.out.println("round of 3.4 is " + Math.round(3.4));

        System.out.println("abs of -3.2 is " + Math.abs(-3.2));

        System.out.println("max of 3.2 and 3.8 is " + Math.max(3.2,3.8));
        System.out.println("min of 3.2 and 3.8 is " + Math.min(3.2,3.8));

        System.out.println("pow of 3.2 and 3.8 is " + Math.pow(3.2,3.8));

        System.out.println("sqrt of 3.2 is " + Math.sqrt(3.2));
        System.out.println("sqrt of 3.8 is " + Math.sqrt(3.8));

        System.out.println("random is " + Math.random());

    }
}
